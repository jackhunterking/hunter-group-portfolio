"""Regenerate shared Codex/Cowork posting artifacts from repo source files."""

from __future__ import annotations

import argparse
import hashlib
import html
import json
import re
from dataclasses import dataclass
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
SKILL_ROOT = REPO_ROOT / ".agents" / "skills" / "jack-ve-tara-posting"
REFERENCES = SKILL_ROOT / "references"
OUTPUT_ROOT = REPO_ROOT / "shared-agent" / "generated"

SKILL_FILE = SKILL_ROOT / "SKILL.md"
VOICE_FILE = REFERENCES / "voice.md"
TURKISH_FILE = REFERENCES / "turkish-characters.md"

SOURCE_FILES = [
    SKILL_FILE,
    VOICE_FILE,
    TURKISH_FILE,
]


@dataclass(frozen=True)
class Platform:
    name: str
    slug: str
    reference: Path
    account: str


def read_text(path: Path) -> str:
    return path.read_text(encoding="utf-8")


def sha256(path: Path) -> str:
    return hashlib.sha256(path.read_bytes()).hexdigest()


def extract_platform_order(skill_text: str) -> list[str]:
    match = re.search(
        r"Generate the Gate 1 copy package in this order:\s*([^\n]+)",
        skill_text,
    )
    if not match:
        raise ValueError("Could not find platform order in posting SKILL.md")
    return [item.strip().rstrip(".") for item in match.group(1).split(",")]


def extract_account(reference_text: str, platform_name: str) -> str:
    match = re.search(r"^Account:\s*(.+)$", reference_text, re.MULTILINE)
    if not match:
        raise ValueError(f"Could not find Account line for {platform_name}")
    return match.group(1).strip()


def load_platforms() -> list[Platform]:
    skill_text = read_text(SKILL_FILE)
    platforms: list[Platform] = []
    for name in extract_platform_order(skill_text):
        slug = name.lower()
        reference = REFERENCES / f"{slug}.md"
        if not reference.exists():
            raise FileNotFoundError(f"Missing platform reference: {reference}")
        account = extract_account(read_text(reference), name)
        platforms.append(Platform(name=name, slug=slug, reference=reference, account=account))
    return platforms


def source_files(platforms: list[Platform]) -> list[Path]:
    files = [*SOURCE_FILES, *(platform.reference for platform in platforms)]
    return sorted(dict.fromkeys(files), key=lambda path: path.as_posix())


def source_digest(platforms: list[Platform]) -> dict[str, object]:
    files = source_files(platforms)
    return {
        "schema_version": 1,
        "source_root": ".agents/skills/jack-ve-tara-posting",
        "files": [
            {
                "path": path.relative_to(REPO_ROOT).as_posix(),
                "sha256": sha256(path),
            }
            for path in files
        ],
    }


def extract_code_block_after_heading(text: str, heading: str) -> str:
    heading_index = text.find(heading)
    if heading_index == -1:
        raise ValueError(f"Could not find heading: {heading}")
    after_heading = text[heading_index:]
    match = re.search(r"```(?:text)?\n(.*?)\n```", after_heading, re.DOTALL)
    if not match:
        raise ValueError(f"Could not find code block after heading: {heading}")
    return match.group(1).strip()


def generated_header(kind: str) -> str:
    return (
        f"<!-- Generated by tools/refresh_agent_bundles.py. Do not edit by hand. -->\n"
        f"<!-- {kind} reads source truth from .agents/skills/jack-ve-tara-posting. -->\n"
    )


def markdown_platform_table(platforms: list[Platform]) -> str:
    lines = ["| Platform | Account | Source file |", "|---|---|---|"]
    for platform in platforms:
        source = platform.reference.relative_to(REPO_ROOT).as_posix()
        lines.append(f"| {platform.name} | `{platform.account}` | `{source}` |")
    return "\n".join(lines)


