def write(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(content, encoding="utf-8")


def main() -> int:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--check", action="store_true", help="report whether generated artifacts are current without writing")
    args = parser.parse_args()

    platforms = load_platforms()
    outputs = {
        OUTPUT_ROOT / "codex-atlas-posting.md": render_codex_bundle(platforms),
        OUTPUT_ROOT / "cowork-claude-chrome-posting.md": render_cowork_bundle(platforms),
        OUTPUT_ROOT / "posting-control.html": render_control_panel(platforms),
        OUTPUT_ROOT / "source-digest.json": json.dumps(source_digest(platforms), indent=2, ensure_ascii=False) + "\n",
    }

    stale = [path for path, content in outputs.items() if not path.exists() or read_text(path) != content]
    if args.check:
        if stale:
            print("Generated artifacts are stale:")
            for path in stale:
                print(f"- {path.relative_to(REPO_ROOT).as_posix()}")
            return 1
        print("Generated artifacts are current.")
        return 0

    for path, content in outputs.items():
        write(path, content)

    print("Refreshed shared posting artifacts:")
    for path in outputs:
        print(f"- {path.relative_to(REPO_ROOT).as_posix()}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
